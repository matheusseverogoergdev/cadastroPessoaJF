    public void addRowToTable() {
        // Cria obj e recebe a modelagem da tabela JtPessoa do JFrame
        DefaultTableModel model = (DefaultTableModel) jtPessoas.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Object rowData[] = new Object[4]; //Cria vetor para as colunas da tabela
        for (Pessoa p: cadPessoas.getAll()) {
            rowData[0] = p.getNomePessoa();
            rowData[1] = ValidaCPF.imprimeCPF(p.getCpf());
            rowData[2] = p.getTelefone();
            if (p.isStatus()) {
                rowData[3] = "Ativo";
            } else {
                rowData[3] = "Inativo";
            }
            model.addRow(rowData);
        }
    }
    
    public boolean validaInputs() {
        String telefone = jtfTelefone.getText(), cpf = jtfCPF.getText();
        if (jtfNome.getText().isBlank() ||
                jtfCPF.getText().isBlank() ||
                jtfEndereco.getText().isBlank() ||
                telefone.isBlank() ||
                jtfIdade.getText().isBlank() ||
                bgStatus.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser"
                    + " preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            jtfNome.requestFocus();
            return false;
        }
        
        if (!ValidaCPF.isCPF(jtfCPF.getText())) {
            JOptionPane.showMessageDialog(this, "CPF inválido!",
            "Erro", JOptionPane.ERROR_MESSAGE);
            jtfCPF.requestFocus();
            return false;
        } else if (cadPessoas.verCPF(jtfCPF.getText())) {
            JOptionPane.showMessageDialog(this, "CPF já cadastrado!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            jtfCPF.requestFocus();
            return false;
        } 
        
        if (telefone.length() != 10 && telefone.length() != 11) {
            JOptionPane.showMessageDialog(this, "Telefone informado está "
                    + "incorreto!",
            "Erro", JOptionPane.ERROR_MESSAGE);
            jtfTelefone.requestFocus();
            return false;
        }
        
        if (!jtfIdade.getText().isBlank()) {
            int idade = Integer.parseInt(jtfIdade.getText());
            if (idade <= 0 || idade > 120) {
                JOptionPane.showMessageDialog(this, "Revise a idade!",
            "Erro", JOptionPane.ERROR_MESSAGE);
                jtfIdade.requestFocus();
                return false;
            }
        }
        
        return true;
    }
    
